class SyncListener(QObject):

    """
        This class is in charge of maintaining in the main thread all the actions of the message bar management as well
         as the message dialogue associated to it.
         :ivar start_signal: Signal to create a new message bar.
         :type start_signal: pyqtSignal
         :ivar message_bar: reference to the current message bar displayed.
         :type message_bar: QMessageBar
    """

    start_signal = pyqtSignal(str, int, int, bool)

    def __init__(self, iface: QgisInterface, dlg: MessagesDialog):
        """
        Constructor.
        :param iface: QgisInterface of the QGIS application.
        :param dlg: message dialogue reference associated with the message bar.
        """
        super().__init__()
        self.iface = iface
        self.dlg = dlg
        self.message_bar = None
        self.start_signal.connect(self._on_start_ui)

    def create_or_update_message_bar(self, msg:str, level:Qgis.MessageLevel = Qgis.Info, time: int = 0,
                                     clear_messages: bool = True):
        """
            Sends the signal to create or update the message bar.
            :param msg: message to be displayed.
            :param level: message level. It changes the color of the message bar.
            :param time: time in milliseconds to clean the new created message bar.
            :param clear_messages: clear the messages in the dialog before creating the new message bar.
        """
        self.start_signal.emit(msg, level, time, clear_messages)

    @pyqtSlot(str, int, int, bool)
    def _on_start_ui(self, msg: str, level: Qgis.MessageLevel, time: int, clear_messages: bool):
        """
            Create or update the message bar, and clears the messages in the dialog.
            :param msg: message to be displayed.
            :param level: message level. It changes the color of the message bar.
            :param time: time in milliseconds to clean the new created message bar.
            :param clear_messages: clear the messages in the dialog before creating the new message bar.
        """
        if clear_messages:
            self.dlg.clear_messages()
        self.iface.messageBar().clearWidgets()
        self.message_bar = None

        self._create_new_bar_and_push(msg, level, time)

    def _create_new_bar_and_push(self, msg: str, level: Qgis.MessageLevel, time: int):
        """
            Creates a new message bar and pushes it into the QGIS interface.
            :param msg: message to be displayed in the message bar.
            :param level: message level. It changes the color of the message bar.
            :param time: time in milliseconds to clean the new created message bar.
        """
        message_bar = self.iface.messageBar().createMessage(msg)
        self.message_bar = message_bar
        btn_messages = QPushButton(self.message_bar)
        btn_messages.setText(tr("Messages"))
        btn_messages.clicked.connect(self._show_messages_dialog)
        self.message_bar.layout().addWidget(btn_messages)
        self.message_bar.setLevel(level)
        self.iface.messageBar().pushWidget(self.message_bar, level)
        if time > 0:
            QTimer.singleShot(time, lambda: self._remove_message(message_bar))

    def _remove_message(self, item):
        """
            Remove the message bar if it is still been displayed.
            :param item: message bar reference that we want to remove.
        """
        if item == self.message_bar:
            self.iface.messageBar().clearWidgets()
            self.message_bar = None


    def _show_messages_dialog(self):
        """
            Message bar button functionality. It shows the message dialog.
        """
        self.dlg.show()



