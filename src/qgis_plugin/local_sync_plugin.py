    def add_action(
        self,
        text: str,
        name: str,
        icon_path: str="",
        callback:Callable=None,
        enabled_flag:bool=True,
        add_to_menu:bool=True,
        add_to_toolbar:bool=True,
        status_tip:str=None,
        whats_this:str=None,
        parent:QWidget=None) -> QAction:

        """Add a toolbar icon to the toolbar.

        :param icon_path: Path to the icon for this action. Can be a resource
            path (e.g. ':/plugins/foo/bar.png') or a normal file system path.
        :param text: Text that should be shown in menu items for this action.
        :param callback: Function to be called when the action is triggered.
        :param name: type of object been initialised.
        :param enabled_flag: A flag indicating if the action should be enabled
            by default. Defaults to True.
        :param add_to_menu: Flag indicating whether the action should also
            be added to the menu. Defaults to True.
        :param add_to_toolbar: Flag indicating whether the action should also
            be added to the toolbar. Defaults to True.
        :param status_tip: Optional text to show in a popup when mouse pointer
            hovers over the action.
        :param parent: Parent widget for the new action. Defaults None.
        :param whats_this: Optional text to show in the status bar when the
            mouse pointer hovers over the action.
        :returns: The action that was created. Note that the action is also
            added to self.actions list.
        """

        if name != "device" and name != "projects" and name != "selected_project":
            icon = QIcon(icon_path)
            action = QAction(icon, text, parent)
            action.triggered.connect(callback)
            action.setEnabled(enabled_flag)
        else:
            if name == "device":
                self.devices_combo = DevicesCombo(self.s_eng, self.sync_listener)
                self.devices_combo.placeholderText = tr("Select device...")
                self.devices_combo.setToolTip(text)
                self.devices_combo.setEnabled(enabled_flag)
                self.devices_combo.setMinimumWidth(200)
                self.devices_combo.currentIndexChanged.connect(self.device_combo_index_changed)

                action = QWidgetAction(parent)
                action.setDefaultWidget(self.devices_combo)

            elif name == "projects":
                icon = QIcon(icon_path)
                action = QAction(icon, text, parent)
                action.triggered.connect(callback)
                action.setEnabled(enabled_flag)
                self.projects_button = action
            else:
                self.selected_project = QLabel()
                self.selected_project.setText("")
                self.c_manager.configuration_changed.connect(self.change_label_value)

                action = QWidgetAction(parent)
                action.setDefaultWidget(self.selected_project)

        if status_tip is not None:
            action.setStatusTip(status_tip)

        if whats_this is not None:
            action.setWhatsThis(whats_this)

        if add_to_toolbar:
            self.plugin_toolbar.addAction(action)

        if add_to_menu:
            self.iface.addPluginToMenu(
                self.menu,
                action)

        if name == "upload" or name == "download" or name == "projects":
            action.setEnabled(False)

        self.actions[name] = action

        return action



    def change_label_value(self):
        if self.selected_project:
            projects = self.c_manager.search_for_projects_in_config()
            if projects:
                if len(projects) == 1:
                    project_list = projects[0]
                else:
                    project_list = ", ".join(projects)

                MESSAGE = QT_TRANSLATE_NOOP(
                    "CartoDruid_Device_Sync",
                    "Current project: <b>{projects}</b>"
                )
                text = tr(MESSAGE).format(projects=project_list)
                self.selected_project.setText(text)
                self.projects_button.setIcon(QIcon(':/resources/carto-folder-update.png'))
                self.projects_button.setText(tr("Change project"))

            else:
                self.selected_project.setText("")
                self.projects_button.setIcon(QIcon(':/resources/carto-folder.png'))
                self.projects_button.setText(tr("Connect to project"))



