    def get_layers_in_edit_mode(self):
        """
            Get the layers that are opened in edit mode.
            :return: List of layers opened and in edit mode currently.
        """
        editable_layers = []
        for layer in QgsProject.instance().mapLayers().values():
            if layer.isEditable():
                editable_layers.append(layer)
        return editable_layers


    def upload(self):
        """
            Start the process of uploading files if there are no layers currently in edit mode. If there are any layers,
            then start the save layers modal dialog.
        """
        editable_layers = self.get_layers_in_edit_mode()
        if not editable_layers:
            if self.devices_combo.currentData():
                self.load_files(False)
        else:
            self.launch_save_modal(editable_layers, False)


    def continue_download(self, stop_continue:bool):
        if not stop_continue:
            self.load_files(True)

    def download(self):
        """
            Start the process of download files if there are no layers currently in edit mode. If there are any layers,
            then start the save layers modal dialog.
        """
        editable_layers = self.get_layers_in_edit_mode()
        if not editable_layers:
            if self.devices_combo.currentData():

                if not self.check_project.check_project_configuration_changed(self.devices_combo.currentData()):
                    self.continue_download(False)
                #self.load_files(True)
        else:
            self.launch_save_modal(editable_layers, True)


    def open_select_project_window(self, second_page:bool = False):

        """
            Open the project wizard, and initialise it.
        """
        if  QgsProject.instance().fileName():

            if not hasattr(self, "project_wizard"):
                self.project_wizard = ProjectWizard(self, self.c_manager,self.p_manager,self.s_eng, self.sync_listener, self.photo_configuration)

            if self.devices_combo.currentData():
                self.project_wizard.initialise(self.devices_combo.currentData(),second_page)
                # show the dialog
                self.project_wizard.exec()
        else:
            QMessageBox.critical(None, tr("Can't open cartodruid project wizard."),
                                 tr("An active project must exist to be able to open this wizard. Open a project or create a new one to complete to be able to use the wizard."))


    def open_config_window(self):
        """
            Open the configuration window.
            Instantiate the configuration dialog if it is the first time this method has been called.
        """
        if not hasattr(self, "conf_dlg"):
            self.conf_dlg = LocalsyncConfPanel(self, self.c_manager, self.photo_configuration)
            self.c_manager.conf_dlg = self.conf_dlg

        # show the dialog
        self.conf_dlg.show()
        # Run the dialog event loop
        self.conf_dlg.open()

        formated_conf = json.dumps(self.c_manager.config, indent=4, ensure_ascii=False)
        formated_photo_conf = json.dumps(self.photo_configuration.config, indent=4, ensure_ascii=False)

        self.conf_dlg.conf_text_edit.setText(formated_conf)
        self.conf_dlg.photo_layers_edit.setText(formated_photo_conf)


    def launch_save_modal(self, layers:list, download:bool):
        """
            Launch the dialog for saving the layers with edit mode activated. After the dialog is closed, depending on
            the result, the download or upload action is restarted.
            :param layers: List of layers to save.
            :param download: indicates whether to download (True) or upload (False).
        """
        if not hasattr(self, "save_dlg"):
            self.save_dlg = LocalsyncSaveLayers()

        if layers:
            model = QStringListModel()
            model.setStringList([layer.name() for layer in layers])

            self.save_dlg.list_view.setModel(model)
            self.save_dlg.list_view.setEditTriggers(QListView.NoEditTriggers)

        # show the dialog
        self.save_dlg.show()

        # Run the dialog event loop
        result = self.save_dlg.exec_()

        if result:
            for layer in layers:
                layer.commitChanges()
            if download:
                self.download()
            else:
                self.upload()

    def load_ended(self):
        """
            Method called when the download/upload of files task end. Reload all the opened layers.
            Reactivate the control of buttons when finished.
        """
        self.block_plugin_actions(False)
        for layer in QgsProject.instance().mapLayers().values():
            layer.reload()
        self.photo_configuration.create_or_update_photo_layers()

            
    def load_terminated(self):
        self.block_plugin_actions(False)

    def load_files(self, pull:bool):
        """
            Start the process of downloading or uploading files between the device and the pc.
            To start the process is necessary that the buttons are not blocked, we have data about the current
            device and the configuration paths exists.
            :param pull: whether to download (True) or upload (False).
        """
        current_device = self.devices_combo.currentData()
        if not self.searching_projects and not self.block_buttons and not current_device.no_data:
            transformed_config = self.c_manager.convert_config_relative_path_to_absolute(self.c_manager.config)
            if self.c_manager.check_if_config_path_exists(transformed_config, True, current_device):
                if QgsProject.instance().fileName():
                    self.block_plugin_actions(True)
                    self.load_files_task = LoadFiles(self.s_eng, transformed_config,
                                                     current_device, pull, self.sync_listener)
                    self.load_files_task.taskCompleted.connect(self.load_ended)
                    self.load_files_task.taskTerminated.connect(self.load_terminated)
                    QgsApplication.taskManager().addTask(self.load_files_task)
                else:
                    self.logger.warning("You need to have an open project in QGIS to use this plugin.")
        else:
            self.logger.warning("A load command was issued, but there is no device selected.")


    def block_plugin_actions(self, block: bool):
        """
            Block plugin actions.
            :param block: whether to block plugin actions.
        """
        #self.projects_button.blockSignals(block)
        self.devices_combo.blockSignals(block)
        self.enable_buttons(not block)
        self.block_buttons = block


    def device_combo_index_changed(self, index):
        self.enable_buttons(self.devices_combo.currentData() != None)


    def enable_buttons(self, enable: bool):
        for button in self.actions:
            if button == "download" or button == "upload" or button == "projects":
                self.actions[button].setEnabled(enable)


    def unload(self):
        """Removes the plugin menu item and icon from QGIS GUI."""
        lcpc2p.launchCartoConfig2ndPage.disconnect()
        cac_check.confirmationReady.disconnect(self.continue_download)
        for action_key in self.actions:
            self.iface.removePluginMenu(
                tr(u'&qgis_plugin'),
                self.actions[action_key])
            self.iface.removeToolBarIcon(self.actions[action_key])
        if self.translator:
            QCoreApplication.removeTranslator(self.translator)